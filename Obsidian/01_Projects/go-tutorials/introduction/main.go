package main

import "fmt"

func main() {

	var car string = "BMW is a good car company"
	var hands int = 2
	fmt.Println(hands)
	fmt.Println(car)

	// bool
	var isGood bool = false
	fmt.Println(isGood)

	// int  int8  int16  int32  int64
	// negative, positive and zero.
	var wheels int = -2
	fmt.Println(wheels)
	// uint uint8 uint16 uint32 uint64 uintptr
	// positive and zero.
	var tires uint = 2
	fmt.Println(tires)

	// float, decimal point float32, float64
	var pi float32 = 3.14
	fmt.Println(pi)
}
